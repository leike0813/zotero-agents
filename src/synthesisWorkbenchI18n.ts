export function formatSynthesisWorkbenchMessage(
  template: string,
  args: Record<string, unknown> = {},
) {
  return template.replace(/%([a-zA-Z0-9_]+)%/g, (match, key) => {
    const value = args[key];
    return value === undefined || value === null ? match : String(value);
  });
}
